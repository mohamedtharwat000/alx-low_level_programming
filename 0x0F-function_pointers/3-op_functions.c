
/**
 * op_add - function that returns the sum of a and b
 * @a: first int input
 * @b: second int input
 * Return: sum of a and b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function that returns the difference of a and b
 * @a: first int input
 * @b: second int input
 * Return: difference of of a and b
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - function that returns the product of a and b
 * @a: first int input
 * @b: second int input
 * Return: product of a and b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - function that returns the result of the division of a by b
 * @a: first int input
 * @b: second int input
 * Return: division of a and b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - function that returns the remainder of the division of a by b
 * @a: first int input
 * @b: second int input
 * Return: remainder of the division of a and b
 */

int op_mod(int a, int b)
{
	return (a % b);
}
